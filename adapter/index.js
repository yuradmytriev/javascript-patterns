/*

Якщо потрібно, щоб один об’єкт міг бути зрозумілим під іншим
інтерфейсом, використовується Адаптер.

Адаптер

Используется для того чтобы объект мог работать с разными интерфейсам
Рнализуется через наследованеи или композицию.

Например у нас есть класс принтер которые печатает строку, но мы хотим печатать массив строк, 
для этого мы созлаем класс адаптер который реализует новый метод для распечатки массивов.

*/